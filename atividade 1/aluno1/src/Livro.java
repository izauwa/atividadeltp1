public class Livro {
 
 
    private int ano;
    private String titulo;
    private String autor;
    public Livro(String titulo, int ano, String autor) {
        this.ano = ano;
        this.titulo = titulo;
        this.autor = autor;
    }
    public int getAno() {
        return ano;
    }
    public void setAno(int string) {
        this.ano = ano;
    }
    public String getTitulo() {
        return titulo;
    }
    public void setTitulo(String i) {
        this.titulo = i;
    }
    public String getAutor() {
        return autor;
    }
    public void setAutor(String b) {
        this.autor = b;
    }
}
    




